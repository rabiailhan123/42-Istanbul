int	ft_str_is_alpha(char *str)
{
	char	c;

	while (*str != '\0')
	{
		c = *str++;
		if (!((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z')))
			return (0);
	}
	return (1);
}
